#!/usr/bin/env python3
"""
This module provides a function to add two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers and returns their sum.

    :param a: The first float number.
    :param b: The second float number.
    :return: The sum of the two float numbers.
    """
    return a + b
